export function uiText(language: UniverLanguage): UiText {
  return texts[language]
}

export function univerLocaleText(language: UniverLanguage) {
  const text = uiText(language)
  return {
    univerPlus: {
      searchFonts: text.searchFonts,
      noMatchingFonts: text.noMatchingFonts,
      noLocalFonts: text.noLocalFonts,
      insertRow: text.insertRow,
      insertColumn: text.insertColumn,
      freezeRow: text.freezeRow,
      freezeColumn: text.freezeColumn,
      unfreeze: text.unfreeze,
      gridlines: text.gridlines,
    },
  }
}
